# ================================================================
# プロンプト解析 → 条件タグ抽出
# ================================================================
def parse_prompt(prompt: str) -> list:
    """プロンプト文字列からキーワードを抽出しタグリストを返す"""
    found_tags = set()
    for kw, tag in KEYWORD_TAG_MAP.items():
        if kw in prompt:
            found_tags.add(tag)
    return sorted(found_tags)


# ================================================================
# 留意点テンプレート選択
# ================================================================
def _score_template(tpl: dict, tags: list) -> int:
    """タグ合致数をスコアとして返す"""
    return sum(1 for t in tpl["tags"] if t in tags)


def select_templates(item_name: str, tags: list, count: int = 3) -> list:
    """
    指定項目のテンプレートをタグ合致スコア順に count 件返す
    Returns: [template_dict, ...]
    """
    bank = TEMPLATE_BANK.get(item_name, [])
    if not bank:
        return []

    scored = sorted(bank, key=lambda t: _score_template(t, tags), reverse=True)

    # スコア0のもの（全く合致しない）でも count 件に満たない場合は補充
    result = []
    seen_ids = set()
    for tpl in scored:
        if len(result) >= count:
            break
        if tpl["id"] not in seen_ids:
            result.append(tpl)
            seen_ids.add(tpl["id"])

    return result[:count]


def match_category(item_description: str, index: int = 0) -> str:
    """項目説明に最も近いカテゴリを返す。スコア0の場合はインデックスで割り当て"""
    scores = {cat: sum(1 for kw in kws if kw in item_description)
              for cat, kws in CATEGORY_KEYWORDS.items()}
    best = max(scores, key=lambda c: scores[c])
    return best if scores[best] > 0 else DEFAULT_ITEMS[index % 3]


def select_templates_for_item(item_description: str, tags: list, count: int = 3, index: int = 0):
    """
    項目の説明文字列からカテゴリを判定し、タグ合致順に count 件返す。
    Returns: (templates: list, matched_category: str)
    """
    cat = match_category(item_description, index)
    return select_templates(cat, tags, count), cat


# ================================================================
# 文字数カウント
# ================================================================
def count_chars(text: str) -> int:
    if not text:
        return 0
    return len(text.replace("\n", "").replace(" ", "").replace("　", ""))


def char_status(note_text: str, reason_text: str) -> dict:
    """文字数チェック結果を辞書で返す"""
    nc = count_chars(note_text)
    rc = count_chars(reason_text)
    total = nc + rc
    # 5行以内の目安: 10ptで約35字/行 × 5行 ≈ 175字（余裕を持って160字を警告）
    if total <= 120:
        status = "ok"
        msg = f"✅ 計{total}字（留意点{nc}字・理由{rc}字）"
    elif total <= 175:
        status = "warn"
        msg = f"⚠️ 計{total}字（5行上限に近づいています）"
    else:
        status = "over"
        msg = f"❌ 計{total}字（5行超過のおそれ。短縮してください）"
    return {"status": status, "message": msg, "note_chars": nc, "reason_chars": rc, "total": total}
